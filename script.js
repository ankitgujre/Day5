// Arithmetic Operator

// + - * / % **

console.log(12+13)
console.log(12-13)
console.log(12*13)
console.log(12/13)
console.log(12%13)
console.log(12**13)
console.log(2**3)

// Assignment Operator

// =, +=, -=, *=, /=, %=

let a = 12;  

a += 13;  // particular value se badhana 25
console.log(a)

a -= 13;
console.log(a)

a *= 13;
console.log(a)

a /= 13;
console.log(a)

a %= 3;
console.log(a)

// Comparision Operator

// ==, ===, !=, !==, >,<, <=, >=

// == not strict compare  ise kabhi bhi use nahi karna
// === strict compare   ise karna hai

// 12 == "12"  yeh true return karega
console.log(12=='12')
console.log(12==='12')   //false

// != never use this
// !== always use this
console.log(12!='12')  // false
console.log(12!=='12')  // true

console.log(12>13)
console.log(12<13)
console.log(12<=13)
console.log(12<=12)
